package com.msubaroda.Anurag.lab1;

public class Q11 {
    public static void main(String[] args) {
        int num1;
        System.out.print("= operator: ");
        num1 = 10;
        System.out.println(num1);

        System.out.print("+= operator: ");
        num1 += 10;
        System.out.println(num1);

        System.out.print("-= operator: ");
        num1 -= 10;
        System.out.println(num1);

        System.out.print("*= operator: ");
        num1 *= 10;
        System.out.println(num1);

        System.out.print("/= operator: ");
        num1 /= 10;
        System.out.println(num1);

        System.out.print("%= operator: ");
        num1 %= 10;
        System.out.println(num1);

    }
}
