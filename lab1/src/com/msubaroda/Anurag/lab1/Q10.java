package com.msubaroda.Anurag.lab1;

public class Q10 {
    public static void main(String[] args) {
        int num1, num2;
        num1 = 10;
        num2 = 20;
        System.out.println("== Operator is: "+ (num1 == num2));
        System.out.println("!= Operator is: "+ (num1 != num2));
        System.out.println("> Operator is: "+ (num1 > num2));
        System.out.println("< Operator is: "+ (num1 < num2));
        System.out.println("<= Operator is: "+ (num1 <= num2));
        System.out.println(">= Operator is: "+ (num1 >= num2));

    }
}
