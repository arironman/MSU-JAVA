public class Q9 {
    static int integer;
    static float floating_point_number;
    static double double_number;
    static long long_number;
    static byte byte_number;
    static String str;
    static boolean bool;

    public static void main(String[] args) {
        System.out.println("The static/default value of integer variable is: "+ integer);
        System.out.println("The static/default value of float variable is: "+ floating_point_number);
        System.out.println("The static/default value of double variable is: "+ double_number);
        System.out.println("The static/default value of long variable is: "+ long_number);
        System.out.println("The static/default value of byte variable is: "+ byte_number);
        System.out.println("The static/default value of String variable is: "+ str);
        System.out.println("The static/default value of boolean variable is: "+ bool);

    }
}
